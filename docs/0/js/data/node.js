import {Token} from './token.js';

// ============================================================================
// 4. AstNodeオブジェクト (不変のTokenを内包し、構造と意味を拡張した完成ノード)
// ============================================================================
export interface AstNode {
    /** 範囲情報・表記情報を完全に共有・保持するための、元となった不変のTokenオブジェクト */
    token: Token;
    
    /** その構文が抽象的に持つ情報を網羅するオブジェクト（意味データ） */
    data: Record<string, any>;

    /** 原稿を1文字の狂いもなく完全復元するための、微細な表記ブレ情報を格納するオブジェクト（復元データ） */
    props: Record<string, any>;

    /** 必要に応じて保持するパース前のプレンスキスト内容（オプショナル） */
    content?: string;

    /** ネストした子ノードの配列（オプショナル） */
    children?: AstNode[];
}

