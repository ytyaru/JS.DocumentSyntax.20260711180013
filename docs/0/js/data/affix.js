// ============================================================================
// 接辞（Affix）のデータ構造定義（配列パターンの厳格な型決定）
// ============================================================================
/** 
 * パターンA：オブジェクトがない版（位置引数のみの単純な配列）
 * 要素は「文字列」か「数値」のみであり、何個でも並べることができる。
 */
export type SimpleAffixList = (string | number)[];
/** 
 * パターンB：オブジェクトがある版（構造化された高度な配列）
 * インデックス0は必ず「文字列（識別子）」、インデックス1は必ず「オブジェクト（構造データ）」
 * それ以降の配列要素（インデックス2以降）は、仕様制限により絶対に存在してはならない。
 */
export type StructuredAffixList = [string, Record<string, any>];
/** 
 * 接辞（Affix）全体を統括するデータ型
 */
export interface AffixData {
    /** 複数行Affix（または1行Affix）が物理的に完全に終了した位置。これ以降から内容テキスト（本文）が始まる */
    end: number;
    
    /** 開始接辞(接頭辞)の解析データ（パターンAまたはパターンBのいずれか） */
    prefix: SimpleAffixList | StructuredAffixList;
    
    /** 終了接辞(接尾辞)の解析データ（パターンAまたはパターンBのいずれか） */
    suffix: SimpleAffixList | StructuredAffixList;
}


